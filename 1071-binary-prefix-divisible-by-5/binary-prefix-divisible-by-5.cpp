class Solution {
public:
    vector<bool> prefixesDivBy5(vector<int>& nums) {
        vector<bool> ans;
        long long num=0;
        for(int i=0;i<nums.size();i++)
        {
             num=(num*2+nums[i])%5;
           
            if(num%5==0)
            {
                ans.push_back(true);

            }
            else{
                ans.push_back(false);
            }
        }
        return ans;
        
    }
};